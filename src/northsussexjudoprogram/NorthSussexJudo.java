    private void addBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBActionPerformed
           // TODO add your handling code here:
           String name = athletes_name.getText();
           String trainingPlan = training_plan.getSelectedItem().toString();
           String currentWeight = current_weight.getText();
           String competitionWeight = competition_weight.getSelectedItem().toString();
           String noOfCompetition = num_competitions.getText();
           String coachingHours = coaching_hours.getSelectedItem().toString();
           String totalCost = total_cost.getText();
           
           if(name.equals("")||name.equals("Input your Name")){
               athletes_name.setText("Input your Name");
           }else if(totalCost.equals("0.0")){
               current_weight.setText("This is Required");
               training_plan.setSelectedIndex(4);
           }else{
               DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
               dtm.addRow(new Object [] {name, trainingPlan, currentWeight, competitionWeight, noOfCompetition, coachingHours, totalCost});
           }
           
    }//GEN-LAST:event_addBActionPerformed

