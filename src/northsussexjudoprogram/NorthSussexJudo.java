    private void checkBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkBActionPerformed
        // TODO add your handling code here:
        double beginnerWeeklyFee = 25.00;
        double intermediateWeeklyFee = 30.00;
        double eliteWeeklyFee = 35.00;
        double privatetuitionHourlyRate = 9.50;
        double competitionEntryFee = 22.00;
        
        String trainingPlan = training_plan.getSelectedItem().toString();
        int numCompetitions = Integer.parseInt(num_competitions.getText());
        int numPrivateCoachingHours=Integer.parseInt(coaching_hours.getSelectedItem().toString());
        String Test = competition_weight.getSelectedItem().toString();
        int competitionWeightCategory;
        if (Test.equals("Over 100")){
            competitionWeightCategory=110;
        }else{
            competitionWeightCategory=Integer.parseInt(Test);
        }
        int currentWeight = Integer.parseInt(current_weight.getText());
        
        // Calculate training fees For a Month
        double trainingFee = 0.00;
            switch (trainingPlan){
                case "Beginner" -> trainingFee = beginnerWeeklyFee * 4; // 4 weeks in a month
                case "Intermediate" -> trainingFee = intermediateWeeklyFee * 4; // 4 weeks in a month
                case "Elite" -> trainingFee = eliteWeeklyFee * 4; // 4 weeks in a month
                default -> System.out.println("Invalid training Plan.");
            }
            
            // Calculate competition fees (only for Intermediate and Elite athletes)
            double competitionFee = 0.0;
            if (trainingPlan.equals("Intermediate")|| trainingPlan.equals("Elite")) {
                competitionFee = numCompetitions * competitionEntryFee;
                competition_fee.setText("£"+String.valueOf(competitionFee));
            }else{
                competition_fee.setText("You aren't Allowed");
                num_competitions.setText("0");
                competition_weight.setSelectedIndex(0);
            }
            
            //Calculate the Private Coaching Fee a For Month
            double privateCoachingFee = numPrivateCoachingHours * privatetuitionHourlyRate * 4;
            
            // Calculate total cost
            double totalCost = trainingFee + privateCoachingFee + competitionFee;
            
            // Compare current weight to competition weight category
            String weightComparison;
            if (currentWeight <= competitionWeightCategory) {
                weightComparison = "Meets Category";
                //It basically indicates that the athlete is qualified to compete in that category 
                //because their weight is within the permitted range for that competition.
            } else {
                weightComparison = "Above Category";
                //The athlete would not be permitted to compete in that competition category 
                //because their weight exceeds the upper limit for that category.
            }
            
            training_fee.setText(String.valueOf("£"+trainingFee));
            if (trainingPlan.equals("--This is Required--")){
                training_fee.setText("Invalid Training Plan");
            }
            coaching_fee.setText("£"+String.valueOf(privateCoachingFee));
            total_cost.setText("£"+String.valueOf(totalCost));
            weight_comparison.setText(weightComparison);
            if (currentWeight==0){
                weight_comparison.setText("-");
            }
            
    }//GEN-LAST:event_checkBActionPerformed

